package com.company;

public class Film {
    private String filmName;
    private String genreFilm;
    private int time;
    private int yearOfMaking;
    private String actors;
    private String language;
    private String country;

    public String setFilmName() {
        return setFilmName();
    }

    public String setGenreFilm() {
        return genreFilm;
    }

    public int setTime() {
        return time;
    }

    public int setYearOfMaking() {
        return yearOfMaking;
    }

    public String setActors() {
        return actors;
    }

    public String setLanguage() {
        return language;
    }

    public String setCountry() {
        return country;
    }
}





